// Test compound assignment operators

// +=
let a = 10;
a += 5;
console.log(a); // Should be 15

// -=
let b = 20;
b -= 7;
console.log(b); // Should be 13

// *=
let c = 6;
c *= 4;
console.log(c); // Should be 24

// /=
let d = 100;
d /= 5;
console.log(d); // Should be 20

// %=
let e = 17;
e %= 5;
console.log(e); // Should be 2

// Compound with expression
let f = 5;
f += 3 * 2;
console.log(f); // Should be 11
